// Finding the Squareroot of a number 
#include <iostream>
#include <cmath>
using namespace std;

int main(){
    cout << "Number\t\t\tSquare Root" << endl; 
    cout << "\n" << endl;
    cout << "4\t\t\t" << sqrt(4) << endl;
    cout << "12.25\t\t\t" << sqrt(12.25) << endl;
    cout << "0.0121\t\t\t" << sqrt(0.0121) << endl;
    return 0;

}

